
#!/bin/zsh
# Start Backend with Virtual Environment
# Usage: ./start-backend.sh

echo "🚀 Starting Nemhem Backend..."
echo "📁 Changing to backend directory..."

cd backend || { echo "❌ Failed to cd backend"; exit 1; }

echo "🐍 Activating virtual environment..."
source .venv/bin/activate || { echo "❌ Virtual env not found. Run Step 2 first"; exit 1; }

echo "📦 Checking installed packages (requests)..."
python -c "import requests; print('✅ requests imported OK')" || { echo "❌ requests import failed"; exit 1; }

echo "🔥 Starting FastAPI server on http://0.0.0.0:8000 (reload mode)..."
echo "Press Ctrl+C to stop."

exec uvicorn main:app --reload --host 0.0.0.0 --port 8000


